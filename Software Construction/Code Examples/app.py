# String methods
text = "Hello World"
print(text.upper())      # "HELLO WORLD"
print(text.lower())      # "hello world"
print(text.replace("World", "Python"))  # "Hello Python"
print(len(text))         # 11
print(text.split())      # ['Hello', 'World']
# String indexing
print(text[0])    # 'H'
print(text[-1])   # 'd'
print(text[0:5])  # 'Hello'




